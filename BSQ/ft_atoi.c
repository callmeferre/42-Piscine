#include<stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	numero;

	numero = 0;
	i = 0;
	while (str[i])
	{
		numero = 10 * numero + str[i] - 48;
		i++;
	}
	return (numero);
}
