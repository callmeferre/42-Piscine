int	ft_atoi(char	*str)
{
	int	value;
	int	n;

	value = 1;
	n = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 12))
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			value *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		 n = (int)(*str - '0') + (n * 10);
		 str++;
	}
	return (n * value);
}
